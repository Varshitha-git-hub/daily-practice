# Addition (+)
#Example: 
a = 10
b = 5
print(a + b,type(a+b)) 
#Real-time example: Calculating the total cost of two items.
item1_cost = 10.99
item2_cost = 5.99
total_cost = item1_cost + item2_cost
print("Total cost: $", total_cost)
print(type(total_cost))


# Subtraction (-)
#Example:
a = 10
b = 5
print(a - b,type(a-b)) 
#Real-time example: Calculating the change owed to a customer.
purchase_amount = 20.00
payment_amount = 25.00
change_owed = payment_amount - purchase_amount
print("Change owed: $", change_owed)
print(type(change_owed))

# Multiplication ()*
#Example:
a = 10
b = 5
print(a * b,type(a*b)) 
#Real-time example: Calculating the total cost of multiple items.
item_cost = 10.99
quantity = 5
total_cost = item_cost * quantity
print("Total cost: $", total_cost)
print(type(total_cost))


# Division (/)
#Example:
a = 10
b = 5
print(a / b,type(a/b)) 
#Real-time example: Calculating the average cost of multiple items.
total_cost = 50.00
quantity = 5
average_cost = total_cost / quantity
print("Average cost: $", average_cost)
print(type(average_cost))


# Modulus (%)
#Example:
a = 10
b = 5
print(a % b,type(a%b)) 
#Real-time example: Calculating the remainder of a division operation.
dividend = 17
divisor = 5
remainder = dividend % divisor
print("Remainder:", remainder)
print(type(remainder))


#Exponentiation ()
#Example:
a = 10
b = 5
print(a ** b,type(a**b)) 
#Real-time example: Calculating the result of raising a number to a power.
base = 2
exponent = 3
result = base ** exponent
print("Result:", result)
print(type(result))


# Floor Division (//)
#Example: 
a = 10
b = 5
print(a // b,type(a//b)) 
#Real-time example: Calculating the result of dividing two numbers and rounding down to the nearest whole number.
dividend = 17
divisor = 5
result = dividend // divisor
print("Result:", result)
print(type(result))
