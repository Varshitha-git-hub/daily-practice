student_score=int(input("enter a number:"))
if student_score>=90:
    print("grade:A")
elif student_score>=80:
    print("grade=:B")
elif student_score>=70:
    print("grade:C")
elif student_score>=60:
    print("grade:D")
else:
    print("grade:E")

